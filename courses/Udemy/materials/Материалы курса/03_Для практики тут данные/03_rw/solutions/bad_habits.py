# Указываем путь к исходному файлу с данными
path = "/home/pyhs/Рабочий стол/Материалы курса/03_Для практики тут данные/03_rw/bad habits.txt"

# Открываем исходный файл для чтения с кодировкой UTF-8
file = open(path, encoding="utf-8")

# Создаем новый файл для записи обработанного текста с кодировкой UTF-8
new_file = open("processed.txt", "w", encoding="utf-8")

# Список для накопления строк одного абзаца
lines = []

# Переменная для хранения текущего "топика" или заголовка темы
topic_name = ""


def add_LF(line, length=80):
    """
    Функция разбивает абзац на строки длиной до 'length' символов,
    добавляя переносы строк '\n'.

    line: str — исходная строка или абзац
    length: int — максимальная длина строки после переноса (по умолчанию 80)
    """
    # Убираем все существующие переносы строк, чтобы не мешали формированию новых
    line = line.replace("\n", "")

    # Если строка уже короткая, просто возвращаем её без изменений
    if len(line) <= 80:
        return line.rstrip()  # rstrip убирает пробелы в конце строки

    # Инициализируем переменную для нового текста с переносами
    new_line = ""
    # start и stop - индексы для срезов
    start = 0
    stop = length - 5  # отнимаем 5 символов на табуляцию(\t) для первой строки

    # Цикл разбивает строку на куски по 'length' символов
    while start < len(line):
        part = line[start:stop]  # берем подстроку текущей длины
        # Если подстрока не короче длины среза и содержит пробелы, разрываем по последнему пробелу
        if len(part) == (stop - start) and " " in part:
            part = part.rsplit(" ", 1)[0]

        # Сдвигаем начальную позицию на длину уже обработанной подстроки + 1 символ для пробела
        start += len(part) + 1
        # Определяем конец следующей подстроки
        stop = start + length
        # Добавляем обработанную подстроку с переносом строки
        new_line += (part + "\n")

    # Убираем лишний перенос в конце и возвращаем результат
    return new_line.rstrip()


# Основной цикл по строкам исходного файла
for line in file:
    # Убираем пробелы и переносы в начале и конце строки
    line = line.strip()

    # Проверяем, начинается ли строка с цифры и содержит '*' или '#' - это заголовок темы
    if line.startswith(tuple("1234567890")) and "*" in line or "#" in line:
        # Убираем цифры, '*', '#' и лишние пробелы
        line = line.strip("1234567890*#. ")
        # Сохраняем как текущий топик для добавления к строкам абзаца
        topic_name = line + ". "
        continue  # переходим к следующей строке

    # Если строка не пустая
    if line:
        # Добавляем к строке текущий топик (если он есть)
        line = topic_name + line
        # Добавляем строку в список lines для формирования абзаца
        lines.append(line)
        # После добавления обнуляем topic_name, чтобы не дублировать
        topic_name = ""
        continue

    # Если строка пустая и список lines не пуст, значит абзац закончился
    if lines:
        # Объединяем все строки абзаца в одну с табуляцией в начале
        paragraph = "\t" + " ".join(lines)
        # Записываем абзац в новый файл, разбитый на строки длиной до 80 символов
        new_file.write(add_LF(paragraph, 80) + "\n")
        # Очищаем список для следующего абзаца
        lines.clear()

# Если после цикла остались строки (например, последний абзац без пустой строки в конце)
if lines:
    paragraph = "\t" + " ".join(lines)
    new_file.write(add_LF(paragraph, 80) + "\n")
    lines.clear()

# Закрываем исходный и новый файл
file.close()
new_file.close()

