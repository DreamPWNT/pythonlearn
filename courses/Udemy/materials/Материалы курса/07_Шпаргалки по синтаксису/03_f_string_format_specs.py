"""Основные числовые спецификаторы"""
value = 42
# Целое число
print(f"{value:d}")     # 42     - десятичное целое число
print(f"{value:04d}")   # 0042   - с ведущими нулями до 4 символов

# С плавающей точкой
pi = 3.14159265  # и тоже амое для целых чисел:
print(f"{pi:.2f}")      # 3.14   - 2 знака после точки
print(f"{pi:8.2f}")     # '    3.14' - всего 8 символов, выравнивание вправо
print(f"{pi:<8.2f}")    # '3.14    ' - выравнивание влево

"""Выравнивание и заполнение"""
n = 42
# Выравнивание по левому краю
print(f"{n:<5}")        # '42   '
# По правому краю
print(f"{n:>5}")        # '   42'
# По правому краю, если n - строка, то по правому краю.
print(f"{n:5}")        # '   42'
# По центру
print(f"{n:^5}")        # ' 42  '
# С заполнением
print(f"{n:*^7}")       # '**42***'

"""Знаки"""
n = 42
m = -42
# Явный плюс
print(f"{n:+d}")        # +42
print(f"{m:+d}")        # -42
# Пробел вместо плюса
print(f"{n: d}")        #  42
print(f"{m: d}")        # -42

"""Числовые группы (разделители)"""
big = 1234567890
print(f"{big:,}")       # 1,234,567,890   - через запятую (en)
print(f"{big:_}")       # 1_234_567_890   - через подчёркивания

"""Процент, строка, символ"""
value = 0.1234
# Процент
print(f"{value:.2%}")   # 12.34%
# Строка
s = "Python"
print(f"{s:.3}")        # Pyt     - только 3 символа
# Символ по ASCII
print(f"{65:c}")        # A

"""Вложенные выражения"""
width = 10
num = 7
print(f"{num:{width}}")  # '         7' - ширина задаётся переменной

"""Новый синтаксис с = (от Python 3.8)"""
x = 10
print(f"{x=}")           # x=10
print(f"{x=:04d}")       # x=0010

"""Новый синтаксис начиная от Python 3.12"""
"""Поддержка !s, !r, !a"""
text = "привет"
print(f"{text!s}")       # str(text)
print(f"{text!r}")       # repr(text)
print(f"{text!a}")       # '\u043f\u0440\u0438\u0432\u0435\u0442' (печатаются просто байты)

"""Комбинированный пример"""
# Табличное форматирование
for i in range(1, 6):
    print(f"{i:>2} * {i:>2} = {i*i:>3}")

"""
# (Редко применяемые, довольно специфичные):
# Мы это будем знать позже.
# Шестнадцатеричное, восьмеричное, двоичное
"""
value = 0x1b  # вот так <<<< это будем знать позже
print(f"{value:x}")     # 2a     - hex (строчные)
print(f"{value:#x}")    # 0x2a   - hex с префиксом
print(f"{value:o}")     # 52     - oct
print(f"{value:#o}")    # 0o52   - oct с префиксом
print(f"{value:b}")     # 101010 - bin
print(f"{value:#b}")    # 0b101010 - bin с префиксом