"""dict - словарь"""

d = {'name': 'Alice', 'age': 25}

# Очищает словарь, удаляя все элементы.
d.clear()  # {}

# Создает копию словаря.
d = {'name': 'Alice', 'age': 25}
copy_d = d.copy()  # {'name': 'Alice', 'age': 25}

# Создает словарь с ключами из указанного итерируемого объекта и значением по умолчанию.
keys = ['name', 'age', 'city']
new_dict = dict.fromkeys(keys, 'unknown')  # {'name': 'unknown', 'age': 'unknown', 'city': 'unknown'}

# Возвращает значение для указанного ключа, если ключ не найден, возвращает значение по умолчанию.
d.get('name')  # 'Alice'
d.get('city', 'Unknown')  # 'Unknown'

# Возвращает пары (ключ, значение) в виде объекта dict_items.
d.items()  # dict_items([('name', 'Alice'), ('age', 25)])

# Возвращает только ключи словаря в виде объекта dict_keys.
d.keys()  # dict_keys(['name', 'age'])

# Удаляет элемент по указанному ключу и возвращает его значение, если ключ не найден, вызывает ошибку.
d.pop('age')  # 25, d: {'name': 'Alice'}

# Удаляет и возвращает последний добавленный элемент (в Python 3.7+ упорядочение сохраняется).
d.popitem()  # ('name', 'Alice'), d: {}

# Добавляет пары (ключ, значение) из другого словаря или итерируемого объекта, существующие ключи перезаписываются.
d.update({'city': 'New York', 'age': 30})  # {'city': 'New York', 'age': 30}

# Возвращает только значения словаря в виде объекта dict_values.
d.values()  # dict_values(['New York', 30])

# Возвращает значение для указанного ключа, если ключ отсутствует, добавляет его со значением по умолчанию и возвращает это значение.
d.setdefault('last_name', 'Unknown')  # 'Unknown' (если ключа 'last_name' нет, он добавится)
