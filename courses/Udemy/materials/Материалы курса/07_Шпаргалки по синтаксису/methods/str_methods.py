"""str - строки"""

string = 'hi therE!)'

# Возвращает копию строки с первым символом в верхнем регистре, остальные символы переводит в нижний.
string.capitalize()  # 'Hi there!)'

string = 'hi there!)'
# Возвращает отцентрованную строку, дополненную пробелами (или указанным символом) до указанной длины.
string.center(20, '-')  # '-----hi there!)-----'

# Возвращает количество вхождений подстроки в строку.
string.count('h')  # 2

# Кодирует строку в байтовый объект с использованием указанной кодировки.
string.encode(encoding='utf-8')  # b'hi there!)'

# Определяет, заканчивается ли строка указанной подстрокой.
string.endswith('!)')  # True

# Находит индекс первого вхождения подстроки в строку, возвращает -1, если подстрока не найдена.
string.find('there')  # 3

# Проверяет, является ли строка допустимым идентификатором Python.
string.isidentifier()  # False

# Проверяет, состоит ли строка только из цифр.
string.isdigit()  # False

# Заменяет подстроки в строке на другие подстроки.
string.replace('hi', 'hello')  # 'hello there!)'

# Возвращает список слов в строке, разделенных пробелами (или указанным разделителем).
string.split()  # ['hi', 'there!)']

# Возвращает копию строки с удаленными пробелами и символами из начала и конца строки.
string.strip(')')  # 'hi there!'

# Возвращает копию строки в нижнем регистре.
string.lower()  # 'hi there!)'

# Возвращает копию строки, где каждое слово начинается с заглавной буквы.
string.title()  # 'Hi There!)'

# Определяет, начинается ли строка с указанной подстроки.
string.startswith('hi')  # True

# Преобразует строку в верхний регистр.
string.upper()  # 'HI THERE!)'

# Возвращает True, если все символы строки являются буквами.
string.isalpha()  # False (из-за пробела и символа '!')

# Возвращает True, если строка состоит только из букв и цифр.
string.isalnum()  # False (из-за пробела и символа '!')

# Возвращает True, если строка содержит только ASCII-символы.
string.isascii()  # True

# Возвращает True, если строка состоит только из пробелов.
string.isspace()  # False

# Возвращает True, если строка написана в нижнем регистре.
string.islower()  # True

# Возвращает True, если строка написана в верхнем регистре.
string.isupper()  # False

# Возвращает True, если слова в строке начинаются с заглавной буквы, а остальные буквы в словах — строчные.
string.istitle()  # False

# Возвращает True, если строка состоит только из десятичных цифр.
string.isdecimal()  # False

# Возвращает True, если строка состоит только из числовых символов. Более широкий охват чем у isdigit.
# Имеется ввиду числовые символы других языков (римские, арабские, китайские и тд.).
string.isnumeric()  # False

# Присоединяет элементы итерируемого объекта, используя строку как разделитель (объединяет строки).
'-'.join(['hi', 'there!'])  # 'hi-there!'

# Увеличивает строку до указанной длины, добавляя символы слева.
string.ljust(15, '-')  # 'hi there!)-----'

# Удаляет начальные пробелы и символы (если указаны).
string.lstrip('h')  # 'i there!)'

# Возвращает кортеж - строку на две части и разделитель (до первого указанного разделителя и после него).
string.partition(' ')  # ('hi', ' ', 'there!)')

# Увеличивает строку до указанной длины, добавляя символы справа.
string.rjust(15, '-')  # '-----hi there!)'

# Возвращает строку в обратном порядке (это срез по индексам, а не метод)(дождитесь соответствующего раздела).
string[::-1]  # ')!ereht ih'

# Находит индекс последнего вхождения подстроки.
string.rfind('e')  # 8

# Возвращает кортеж: до последнего указанного разделителя, сам разделитель, и оставшуюся строку.
string.rpartition(' ')  # ('hi', ' ', 'there!)')

# Разделяет строку справа на указанное количество частей.
string.rsplit(' ', 1)  # ['hi', 'there!)']

# Удаляет конечные пробелы и символы (если указаны).
string.rstrip('!)')  # 'hi there'

# Разбивает строку на части, разделенные переносами строк.
multiline_string = 'line1\nline2\nline3'
multiline_string.splitlines()  # ['line1', 'line2', 'line3']

# Преобразует строку, чередуя регистры: строчные буквы становятся заглавными и наоборот.
string.swapcase()  # 'HI THERE!)'

# Добавляет 0 для выравнивания строки.
string.zfill(15)  # '000000hi there!)'

# Задает табуляцию в строке (по умолчанию ширина табуляции — 8 пробелов).
string_with_tabs = 'hi\tthere'
string_with_tabs.expandtabs(4)  # 'hi  there'

# Возвращает строку в нижнем регистре, оптимизированную для сравнения символов Unicode.
string_case = 'Straße'
string_case.casefold()  # 'strasse'

# Создает таблицу преобразований для использования с методом translate.
trans_table = str.maketrans('aeiou', '12345')
'hello world'.translate(trans_table)  # 'h2ll4 w4rld'

# Форматирует строку, используя словарь или сопоставимый объект для подстановки.
template = '{name} is {age} years old'
template.format_map({'name': 'Alice', 'age': 25})  # 'Alice is 25 years old'

# Проверяет, можно ли напечатать строку (например, она не содержит управляющих символов).
string = 'hi there!)'
string.isprintable()  # True

# Удаляет указанный префикс из строки, если префикс не найден, возвращает строку без изменений.
string_with_prefix = 'hi there!)'
string_with_prefix.removeprefix('hi ')  # 'there!)'

# Удаляет указанный суффикс из строки, если суффикс не найден, возвращает строку без изменений.
string_with_suffix = 'hi there!)'
string_with_suffix.removesuffix(' there!)')  # 'hi'

# Преобразует строку, используя таблицу преобразований (например, созданную методом maketrans).
trans_table = str.maketrans('abc', '123')
'abcabc'.translate(trans_table)  # '123123'

# Форматирует строку подставляя переданные значения.
name = 'Alice'
age = 25
'Name - {}, age - {}'.format(name, age) # 'Name - Alice, age - 25'
