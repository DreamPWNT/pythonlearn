import math

a = int(input('Enter first int number: '))
b = int(input('Enter second int number: '))

print(f'a + b = {a + b}')
print(f'a - b = {a - b}')
print(f'a * b = {a * b}')
print(f'a / b = {a / b}')
print(f'a % b = {a % b}')
print(f'log10(a) = {math.log10(a)}')
print(f'a ** b = {a ** b}')
