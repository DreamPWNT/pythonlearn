mark = input('Enter student mark: ')

if mark == 'A+':
    print('Mark value = 4.0')
elif mark == 'A':
    print('Mark value = 4.0')
elif mark == 'A-':
    print('Mark value = 3.7')
elif mark == 'B+':
    print('Mark value = 3.3')
elif mark == 'B':
    print('Mark value = 3.0')
elif mark == 'B-':
    print('Mark value = 2.7')
elif mark == 'C+':
    print('Mark value = 2.3')
elif mark == 'C':
    print('Mark value = 2.0')
elif mark == 'C-':
    print('Mark value = 1.7')
elif mark == 'D+':
    print('Mark value = 1.3')
elif mark == 'D':
    print('Mark value = 1.0')
elif mark == 'F':
    print('Mark value = 0.0')
else:
    print('Incorrect mark input!')
